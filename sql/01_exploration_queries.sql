-- =========================================
-- QUERY 5 : CUSTOMER ORDER FREQUENCY DISTRIBUTION
-- =========================================

-- Business Question:
-- What does the customer order frequency distribution look like?

-- Why This Matters:
-- Frequency distribution helps evaluate customer loyalty,
-- repeat purchasing behavior, and churn risk.

-- SQL Concepts Used:
-- CTE, CASE WHEN, GROUP BY, COUNT DISTINCT

-- Key Finding:
-- The largest customer segment consists of repeat buyers
-- placing between 2 and 5 orders.

WITH customer_orders AS (
    SELECT 
        customer_id,
        COUNT(DISTINCT invoice) AS order_count
    FROM orders
    GROUP BY customer_id
)
SELECT 
    CASE 
        WHEN order_count = 1 THEN 'One-time buyers'
        WHEN order_count BETWEEN 2 AND 5 THEN 'Repeat buyers'
        WHEN order_count BETWEEN 6 AND 10 THEN 'Frequent buyers'
        WHEN order_count > 10 THEN 'Loyal buyers'
        ELSE 'Other'
    END AS order_frequency_bucket,
    COUNT(*) AS customer_count
FROM customer_orders
GROUP BY 
    CASE 
        WHEN order_count = 1 THEN 'One-time buyers'
        WHEN order_count BETWEEN 2 AND 5 THEN 'Repeat buyers'
        WHEN order_count BETWEEN 6 AND 10 THEN 'Frequent buyers'
        WHEN order_count > 10 THEN 'Loyal buyers'
        ELSE 'Other'
    END;

-- Business Observation:
-- The largest customer segment consists of 2,454 repeat buyers
-- who placed between 2 and 5 orders.
-- The business also has 925 frequent buyers
-- and 876 loyal buyers who placed more than 10 orders.
-- However, 1,623 customers purchased only once,
-- indicating potential customer retention challenges
-- and opportunities to improve repeat purchasing behavior.


-- =========================================
-- QUERY 6 : CUSTOMER LIFECYCLE LENGTH
-- =========================================

-- Business Question:
-- How long do repeat customers typically remain active?

-- Why This Matters:
-- Understanding customer lifecycle length provides context
-- for interpreting recency and retention behavior.

-- SQL Concepts Used:
-- CTE, DATEDIFF, MIN(), MAX(), AVG(), COUNT DISTINCT

-- Key Finding:
-- Repeat customers remain active for approximately 378 days on average.

WITH customer_lifecycle AS (
    SELECT 
        customer_id,
        MIN(DATE(invoicedate)) AS first_purchase,
        MAX(DATE(invoicedate)) AS last_purchase,
        COUNT(DISTINCT invoice) AS total_orders
    FROM orders
    GROUP BY customer_id
)

SELECT 
    AVG(DATEDIFF(last_purchase, first_purchase)) AS avg_days,
    MIN(DATEDIFF(last_purchase, first_purchase)) AS min_days,
    MAX(DATEDIFF(last_purchase, first_purchase)) AS max_days
FROM customer_lifecycle
WHERE total_orders > 1;

-- Business Observation:
-- Customers who purchased more than once remained active
-- for approximately 378 days on average.
-- Some customers placed repeat orders on the same day,
-- while others remained engaged for nearly two years.
-- This wide lifecycle range highlights major differences
-- in customer loyalty and retention behavior.


-- =========================================
-- QUERY 7 : TOP COUNTRY REVENUE CONTRIBUTION
-- =========================================

-- Business Question:
-- Which countries contribute the highest revenue,
-- and how concentrated is the business geographically?

-- Why This Matters:
-- Geographic dependency creates revenue concentration risk
-- and influences expansion strategy decisions.

-- SQL Concepts Used:
-- CTE, WINDOW FUNCTION, GROUP BY, SUM(), COUNT DISTINCT

-- Key Finding:
-- The United Kingdom contributes more than 82%
-- of total business revenue.

WITH country_stats AS (
    SELECT 
        country,
        COUNT(DISTINCT customer_id) AS total_customers,
        SUM(revenue) AS total_revenue
    FROM orders
    GROUP BY country
)
SELECT
    country,
    total_customers,
    total_revenue,
    ROUND(total_revenue / total_customers, 2) AS avg_revenue_per_customer,
    ROUND(total_revenue / SUM(total_revenue) OVER() * 100, 2) AS revenue_pct
FROM country_stats
ORDER BY total_revenue DESC
LIMIT 5;

-- Business Observation:
-- The United Kingdom contributes approximately 82.82%
-- of total business revenue with over 14.38 million in sales.
-- This creates strong geographic concentration risk.
-- Countries such as EIRE and the Netherlands
-- generate extremely high average revenue per customer,
-- indicating the presence of highly valuable international customers.

