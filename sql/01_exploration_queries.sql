-- QUERY 1 : BUSINESS DATA HEALTH CHECK
-- =========================================

-- Business Question:
-- What is the overall scale and structure of the business dataset?

-- Why This Matters:
-- Every retention and RFM analysis requires a clear understanding
-- of the total customers, orders, revenue, and transaction timeline.

-- SQL Concepts Used:
-- COUNT(), COUNT DISTINCT, MIN(), MAX(), SUM(), AGGREGATION

-- Key Finding:
-- The business generated approximately 17.37 million in revenue
-- across 779K+ transaction records and nearly 37K unique orders.

SELECT 
    COUNT(*) AS total_rows,
    COUNT(DISTINCT customer_id) AS total_customer_id,
    COUNT(DISTINCT invoice) AS unique_orders,
    MIN(DATE(invoicedate)) AS earliest_date,
    MAX(DATE(invoicedate)) AS latest_date,
    SUM(revenue) AS total_revenue
FROM orders;


-- Business Observation
-- The dataset contains 779,425 cleaned transactional records generated by 5,878 unique customers across 36,969 unique orders.
-- The difference between total rows and unique orders indicates that customers frequently purchase multiple products withina single transaction, 
-- reflecting realistic retail purchasing behavior.
-- The transactional history spans from December 2009 to December 2011 providing nearly two years of customer activity suitable for
-- retention, cohort, and RFM analysis.
-- During this period, the business generated approximately 17.37 million in total revenue, indicating a large-scale retail operation
-- with substantial customer purchasing activity.


-- =========================================
-- QUERY 2 : GEOGRAPHIC REVENUE CONCENTRATION
-- =========================================

-- Business Question:
-- Which countries contribute the highest revenue
