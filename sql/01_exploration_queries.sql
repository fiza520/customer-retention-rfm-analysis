-- Business question: Do 10% of customers generate the majority of revenue?
-- SQL concepts: CTE, NTILE window function, GROUP BY, percentage

-- Key Finding:
-- The top 10% of customers generate 63.9% of total revenue,
-- while the top 20% generate 77.2%, indicating strong revenue
-- concentration among a small group of high-value customers.

WITH customer_revenue AS (
    SELECT
        customer_id,
        SUM(revenue) AS total_revenue
    FROM orders
    GROUP BY customer_id
),
deciles AS (
    SELECT
        customer_id,
        total_revenue,
        NTILE(10) OVER (ORDER BY total_revenue DESC) AS revenue_decile
    FROM customer_revenue
)
SELECT
    revenue_decile,
    COUNT(*)                                          AS customers,
    ROUND(SUM(total_revenue), 2)                      AS segment_revenue,
    ROUND(SUM(total_revenue) * 100.0 /
          SUM(SUM(total_revenue)) OVER (), 1)         AS revenue_pct,
    ROUND(SUM(SUM(total_revenue)) OVER
          (ORDER BY revenue_decile
           ROWS BETWEEN UNBOUNDED PRECEDING
           AND CURRENT ROW) * 100.0 /
          SUM(SUM(total_revenue)) OVER (), 1)         AS cumulative_pct
FROM deciles
GROUP BY revenue_decile
ORDER BY revenue_decile;

-- Business Observation:

-- Revenue is highly concentrated among a small percentage
-- of customers. The top 10% of customers (Revenue Decile 1)
-- generated approximately 11.11 million in revenue,
-- contributing 63.9% of total business revenue.

-- When the top 20% of customers are combined,
-- they account for 77.2% of total revenue,
-- which is very close to the classic Pareto Principle
-- where a small percentage of customers drive the majority
-- of business performance.

-- The top 30% of customers contribute 85% of total revenue,
-- indicating that revenue generation is heavily dependent
-- on a relatively small group of high-value customers.

-- This creates customer concentration risk, as losing even
-- a small number of these high-value customers could have
-- a significant impact on overall revenue.

-- The business should prioritize retention, loyalty programs,
-- and personalized engagement strategies for top-spending
-- customers to protect a substantial portion of revenue.

-- At the same time, opportunities exist to increase the value
-- of lower-spending customer segments and reduce dependency
-- on the highest-value customers.