-- QUERY 8 : AT-RISK HIGH-VALUE CUSTOMERS
-- =========================================

-- Business Question:
-- Which previously valuable customers
-- have become inactive in the last 90 days?

-- Why This Matters:
-- Identifying high-value inactive customers
-- helps prevent revenue loss through retention strategies.

-- SQL Concepts Used:
-- CTE, DATEDIFF, CROSS JOIN, MAX(), GROUP BY, ORDER BY, LIMIT

-- Key Finding:
-- Several high-revenue customers have become inactive,
-- indicating potential churn risk and future revenue exposure.

WITH customer_summary AS (
    SELECT 
        customer_id,
        MAX(DATE(invoicedate)) AS last_order_date,
        SUM(revenue) AS total_revenue,
        COUNT(DISTINCT invoice) AS total_orders
    FROM orders
    GROUP BY customer_id
),

max_date AS (
    SELECT MAX(DATE(invoicedate)) AS dataset_max_date
    FROM orders
)

SELECT 
    cs.customer_id,
    cs.last_order_date,
    cs.total_revenue,
    cs.total_orders
FROM customer_summary cs
CROSS JOIN max_date md
WHERE DATEDIFF(md.dataset_max_date, cs.last_order_date) > 90
ORDER BY cs.total_revenue DESC
LIMIT 50;

-- Business Observation:
-- Several previously high-value customers
-- have not placed any orders within the last 90 days,
-- indicating strong churn risk.
-- Customer 12346 generated approximately 77.5K in revenue
-- before becoming inactive.
-- Customer 17850 placed 155 orders
-- but has not purchased since December 2010,
-- representing a significant loss of repeat purchasing activity.
-- These customers should be prioritized
-- for re-engagement and retention campaigns.



-- Business question: How much revenue depends on repeat buyers vs one-time buyers?
-- SQL concepts: CTE, CASE WHEN, SUM, window function percentage

WITH customer_type AS (
    SELECT
        customer_id,
        SUM(revenue)              AS customer_revenue,
        COUNT(DISTINCT invoice)   AS order_count
    FROM orders
    GROUP BY customer_id
)
SELECT
    CASE
        WHEN order_count = 1 THEN 'One-time buyers'
        ELSE 'Repeat buyers'
    END                                          AS buyer_type,
    COUNT(*)                                     AS customer_count,
    ROUND(SUM(customer_revenue), 2)              AS total_revenue,
    ROUND(SUM(customer_revenue) * 100.0 /
          SUM(SUM(customer_revenue)) OVER (), 1) AS revenue_share_pct
FROM customer_type
GROUP BY 1
ORDER BY total_revenue DESC;

