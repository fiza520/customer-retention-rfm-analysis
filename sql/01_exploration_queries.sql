-- =========================================
-- QUERY 3 : MONTHLY REVENUE & ORDER TREND
-- =========================================

-- Business Question:
-- Is the business showing seasonal purchasing behavior
-- or stable linear growth over time?

-- Why This Matters:
-- Understanding seasonality helps improve inventory planning,
-- campaign timing, and customer engagement strategies.

-- SQL Concepts Used:
-- DATE FUNCTIONS, GROUP BY, COUNT DISTINCT, SUM(), ORDER BY

-- Key Finding:
-- Revenue and order volume spike significantly
-- during September–November,
-- indicating strong seasonal demand patterns.

SELECT 
    YEAR(invoicedate) AS order_year,
    MONTHNAME(invoicedate) AS month_name,
    COUNT(DISTINCT invoice) AS total_orders,
    SUM(revenue) AS revenue
FROM orders
GROUP BY YEAR(invoicedate), MONTH(invoicedate), MONTHNAME(invoicedate)
ORDER BY order_year, MONTH(invoicedate);

-- Business Observation:
-- Monthly revenue and order volume show clear seasonal fluctuations
-- rather than steady growth.
-- Revenue increased from approximately 558K in January 2010
-- to over 1.16M in November 2010.
-- A similar pattern appears in 2011,
-- where revenue again crossed 1M during October–November.
-- These repeated spikes strongly suggest holiday or festive season demand.



